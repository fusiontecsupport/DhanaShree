CREATE VIEW [dbo].[VW_REPAIR_SLABMASTER_FLX_ASSGN]
AS
SELECT  REPAIRSLABMASTER.TARIFFMID,REPAIRSLABMASTER.RSLABMID,REPAIRSLABMASTER.RSLABMDATE,
        isnull(REPAIRSLABMASTER.STMRID,0) as  STMRID,REPAIRSLABMASTER.CUSRID,REPAIRSLABMASTER.COMPYID,
		REPAIRSLABMASTER.RPRTID,REPAIRSLABMASTER.RSLABHRS,REPAIRSLABMASTER.WRMMODE,
		REPAIRSLABMASTER.RSLABHAMT,REPAIRSLABMASTER.RSLABMAMT,REPAIRSLABMASTER.RSLABTAMT
		
FROM  REPAIRSLABMASTER INNER JOIN 
EMPTY_TARIFFMASTER (NOLOCK) ON REPAIRSLABMASTER.TARIFFMID = EMPTY_TARIFFMASTER.TARIFFMID LEFT JOIN  
dbo.CATEGORYMASTER (NOLOCK) ON REPAIRSLABMASTER.STMRID = dbo.CATEGORYMASTER.CATEID



GO


